
public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World");//String 문자열/큰다은표"" 문자열 작성
	  //System.out.println('H');//Character 문자 -한글자/작은다운표'' 문자 
		System.out.println("Hello"
	  + "World");//줄바꿈 안됨
		
		// new line
		System.out.println("Hello \nWorld");//(\n)=new line 새로운 줄 역슬래시 사용
		System.out.println("Hello \"world\""); //Helod "World" 역슬래시앞에 다운표 일반 문자 그럼으로 출력됨
	}

}
