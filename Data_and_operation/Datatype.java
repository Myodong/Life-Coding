public class Datatype{
	public static void main(String[] args) {
		System.out.println(6);//Nuber 숫자표시
		System.out.println("six");//String 문자표시
		
		System.out.println("6");//String 6 ""안에 있으면 문자로 취급
		System.out.println(6+6);//12
		System.out.println("6"+"6");//66 문자와 문자를 합쳐준거 
		System.out.println(6*6);//36
		// System.out.println("6"*"6"); 문자열 곱셈이기 때문에 값이 나오지 않는다
		System.out.println("1111".length());//문자열에 길이를 알려줌 4
		// System.out.println(111.length()); // 숫자로 인식하기 때문에 값이 나오지 않는다
		
	}
	
}